1. About this Notice

This Privacy Notice provides information on how Jumia collects and processes your personal data when you visit our website or mobile applications. It sets out what we do with your personal data and how we keep it secure and explains the rights that you have in relation to your personal data. 

 <br>

2. Who We Are

Jumia is the leading pan-African e-commerce platform. Our platform consists of our marketplace, which connects sellers with consumers, our logistics service, which enables the shipment and delivery of packages from sellers to consumers, and our payment service, which facilitates transactions among participants active on our platform in selected markets.

This website and/or mobile app is operated by a member of the Jumia group of companies, the ultimate holding company of which is Jumia Technologies AG. Information on our subsidiaries can be found on our website.

Any personal data provided or collected by Jumia is controlled by the subsidiary that the website and/or mobile app relates to. 
<br>
 

3. The Data We Collect About You?

Personal data means any information that can be used to identify directly or indirectly a specific individual. We collect your personal data in order to provide tailored products and services and in order to analyse and continually improve our products and services. We may collect, use, store and transfer different kinds of personal data for marketing and personal data optimization purposes. Jumia also uses Google Digital Marketing to propose targeted offers for certain products and services to our customers.

 

You provide us with your personal data when you register your personal details on our website and mobile platforms and transact with the same. 

The personal data we collect includes:

Information you provide to us: We receive and store the information you provide to us including your identity data, contact data, biometric data, delivery address and financial data. These types of personal data may include:

contact details (such as your name, postal addresses, phone numbers and email addresses),

demographic information (such as your date of birth, age or age range and gender),

online registration information (such as your password and other authentication information),

payment information (such as your credit card information and billing address),

information provided as part of online questionnaires (such as responses to any customer satisfaction surveys or market research),

competition entries/submissions, and

in certain cases your marketing preferences.

 

Information we automatically collect/generate or obtain from third parties: We automatically collect and store certain types of information regarding your use of the Jumia marketplace including information about your searches, views, downloads and purchases. In addition, we may receive information about you from third parties including our carriers; payment service providers; merchants/brands; and advertising service providers.

 

These types of personal data may relate to your device (such as your PC, tablet or other mobile device), your use of our websites and apps (as well as certain third party websites with whom we have partnered), and/or your personal preferences, interests, or geographic location. Examples of these types of information include:

name and age (or predicted age range),

information about your device, operating system, browser and IP address,

unique identifiers associated with your device,

details of web pages that you have visited,

which products you have looked at online (including information about products you have searched for or viewed, purchased or added to an online shopping basket),

how long you spend on certain areas of a website or app together with the date and time of your visit/usage,

personal data contained within user-generated content (such as blogs and social media postings),

social media user name or ID, and

social media profile photo and other social media profile information (such as number of followers).

 

We strive to provide you with choices regarding the Personal Data that you provide to us. Where required by law, if you wish to have your Personal Data used by Jumia to provide you with a personalized experience/targeted advertising & content, you can indicate so through the relevant tick-box(es) located on the registration form or by answering the question(s) presented by Jumia representatives. If you decide that you no longer wish to benefit from this personalization, you can opt-out or adjust your preferences at any time by closing your account or by sending an email to Nigeria.legal@jumia.com. You can close your account by clicking on this link and following the instructions. Once your account is closed, all products and services that you access through your account will no longer be available.

